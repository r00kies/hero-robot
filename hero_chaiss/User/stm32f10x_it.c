/*******************************************************************************
* Function Name  : SysTick_Handler
* Description    : This function handles SysTick Handler.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void SysTick_Handler(void)
{
  if(SystemInitReady == 1)
  {
    SystemCounter++;
    if(SystemCounter > SYSTEM_COUNT)
    {SystemCounter = 1;}
		
    if((SystemCounter % COUNT_1000HZ) == 0)	   
    {Moto_pid_1000Hz = 1;}
		
    if((SystemCounter % COUNT_500HZ) == 0)	   
    {Attitude_500Hz = 1;}

    if((SystemCounter % COUNT_100HZ) == 0)
    {Motor_100Hz = 1;}

	  if((SystemCounter % COUNT_10HZ) == 0)
  	{Com_10Hz = 1;}
		if((SystemCounter % COUNT_2HZ) == 0)
	  {Set_2Hz = 1;} 
	
  }
}

